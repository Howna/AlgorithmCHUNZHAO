class Solution {
    public void solve(char[][] board) {
        int row = board.length;
        int column = board[0].length;

        for (int i = 0; i < row; i++) {
            for (int j = 0; j < column; j++) {
                fill(board, 0, j);
                fill(board, row-1, j);
            }

            fill(board, i, 0);
            fill(board, i, column-1);
        }

        for (int i = 0; i < row; i++) {
            for (int j = 0; j < column; j++) {
                if (board[i][j] == 'O') {
                    board[i][j] = 'X';
                }

                if (board[i][j] == '#') {
                    board[i][j] = 'O';
                }
            }
        }
    }

    void fill(char[][] board, int x, int y) {
        if (x < 0 || x >= board.length || y < 0 || y >= board[0].length || board[x][y] == 'X' || board[x][y] == '#') {
            return;
        }

        board[x][y] = '#';
        fill(board, x-1, y);
        fill(board, x+1, y);
        fill(board, x, y-1);
        fill(board, x, y+1);

    }
}
